            myChart.setOption({
                title:{
                    text:'华北地区性别占比',
                    subtext:'数据来自网络'
                },
                legend: {},
                tooltip: {},
                dataset: {
                    source: [
                        ['product', '男','女'],
                        ['河北',36430286,35423924],
                        ['山西',18338760,17373341],
                        ['内蒙古',12838243,11868048],
                        ['北京',10126430,9485938],
                        ['天津',6907091,6031602]
                    ]
                },
                xAxis: {type: 'category'},
                yAxis: {},
                // Declare several bar series, each will be mapped
                // to a column of dataset.source by default.
                series: [
                    {type: 'bar'},
                    {type: 'bar'}
                ]
            });
        }
    }
</script>